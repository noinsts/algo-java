import java.util.Scanner;

/*
Ось технічне завдання (ТЗ) до коду програми EvenOddSum.java:
Технічне завдання: Програма для обчислення сум парних і непарних чисел
Опис:

Розробити програму на мові Java, яка обчислює суму парних і непарних чисел у масиві, що вводиться користувачем, та виконує порівняльний аналіз цих сум.
Функціональні вимоги:

    Введення даних:
        Користувач повинен спочатку ввести кількість елементів масиву (n).
        Далі користувач вводить n цілих чисел (елементи масиву).
    Валідація введення:
        Якщо n = 0, програма повинна вивести повідомлення "Масив порожній, немає що рахувати!" і завершити роботу.
        Якщо n < 0, програма повинна вивести повідомлення "Кількість елементів масиву не може бути від'ємною!" і завершити роботу.
    Обчислення:
        Програма розділяє введені числа на парні та непарні.
        Програма обчислює суму парних чисел (evenSum) та суму непарних чисел (oddSum).
    Виведення результатів:
        Вивести значення суми парних чисел.
        Вивести значення суми непарних чисел.
        Здійснити порівняльний аналіз і вивести:
            Якщо сума парних чисел більша, вивести на скільки вона більша.
            Якщо сума непарних чисел більша, вивести на скільки вона більша.
            Якщо суми рівні, вивести відповідне повідомлення.

 */

public class EvenOddSum {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);

        int n = sc.nextInt();

        if (n <= 0) {
            if (n == 0) {
                System.out.println("Масив порожній, немає що рахувати!");
            } else {
                System.out.println("Кількість елементів масиву не може бути від'ємною!");
            }
            sc.close();
            return;
        }

        int evenSum = 0, oddSum = 0;

        for (int i = 0; i < n; i++){
            int num = sc.nextInt();

            if (num % 2 == 0){
                evenSum += num;
            } else {
                oddSum += num;
            }
        }

        System.out.println();

        System.out.println("Сума парних чисел: " + evenSum);
        System.out.println("Сума непарних чисел: " + oddSum);

        System.out.println();

        if (evenSum > oddSum){
            System.out.println("Сума парних чисел більша за непарні на " + (evenSum - oddSum));
        }
        else if (oddSum > evenSum) {
            System.out.println("Сума непарних чисел більша за парні на " + (oddSum - evenSum));
        }
        else {
            System.out.println("Сума парних чисел така сама, як і непарних");
        }

        sc.close();
    }
}
